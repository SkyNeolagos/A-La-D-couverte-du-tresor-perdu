public class Item {
    private int id;
    private int type;
    private String nomItem;
    
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public String getNomItem() {
		return nomItem;
	}
	public void setNomItem(String nomIteme) {
		this.nomItem = nomIteme;
	} 
	
	
}
